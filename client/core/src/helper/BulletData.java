package helper;

public class BulletData {
    public int id;
    public float x, y;
    public float speedBullet;

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }

    public float getSpeedBullet() { return speedBullet; }

    public int getId() {
        return id;
    }
}
